import {  Bounce, toast } from 'react-toastify';
import 'react-toastify/dist/ReactToastify.css';


export function ToastSuccess(message){
    toast.success(message, {
        position: "top-right",
        autoClose: 5000,
        hideProgressBar: false,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: true,
        progress: undefined,
        theme: "light",
        transition: Bounce,
    });
}
